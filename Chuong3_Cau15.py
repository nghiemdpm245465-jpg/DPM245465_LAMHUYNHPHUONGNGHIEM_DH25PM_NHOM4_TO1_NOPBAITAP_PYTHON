#Câu 15: Giải thích cách chạy các dòng lệnh range
""""
(a) range(5)
→ Tạo dãy từ 0 đến 4 (mặc định bắt đầu 0, bước 1).

(b) range(5, 10)
→ Tạo dãy từ 5 đến 9 (bắt đầu 5, dừng trước 10).

(c) range(5, 20, 3)
→ Bắt đầu từ 5, mỗi lần cộng 3, dừng trước 20.

(d) range(20, 5, -1)
→ Bắt đầu từ 20, mỗi lần trừ 1, dừng trước 5.

(e) range(20, 5, -3)
→ Bắt đầu từ 20, mỗi lần trừ 3, dừng trước 5.

(f) range(10, 5)
→ Không có phần tử nào vì 10 > 5 mà bước mặc định là +1.

(g) range(0)
→ Không có phần tử nào, vì bắt đầu và kết thúc đều là 0.

(h) range(10, 101, 10)
→ Bắt đầu từ 10, mỗi lần cộng 10, dừng trước 101.

(i) range(10, -1, -1)
→ Bắt đầu từ 10, mỗi lần trừ 1, dừng trước -1.

(j) range(-3, 4)
→ Bắt đầu từ -3, mỗi lần cộng 1, dừng trước 4.

(k) range(0, 10, 1)
→ Bắt đầu từ 0, mỗi lần cộng 1, dừng trước 10.
"""